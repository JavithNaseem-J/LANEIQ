from typing import Optional
from pydantic import BaseModel, Field


class RouteOption(BaseModel):
    strategy: str
    total_cost: float
    cost_reduction_vs_baseline: float


class ExceptionSummary(BaseModel):
    shipment_id: str
    exception_type: str
    delay_hours: int
    original_mode: str
    new_mode: str
    new_cost: float


class OptimisationResult(BaseModel):
    """Schema for the final structured output of the LANEIQ agent pipeline."""

    selected_strategy: str = Field(description="Name of the selected routing strategy")
    selected_cost_usd: float = Field(description="Total cost of the selected route in USD")
    cost_reduction_pct: float = Field(
        description="% cost reduction vs greedy baseline (negative = VRP was more expensive)"
    )
    num_vehicles_used: int = Field(description="Number of vehicles / routes in selected solution")
    exceptions_detected: int = Field(description="Number of delay exceptions detected")
    exception_summaries: list[ExceptionSummary] = Field(default_factory=list)
    all_options: list[RouteOption] = Field(default_factory=list)
    final_report: str = Field(description="Plain-English summary generated by the Report Agent")
    error: Optional[str] = Field(default=None)
